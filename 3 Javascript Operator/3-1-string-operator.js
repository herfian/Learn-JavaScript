// Operasi ini biasa dikenal dengan sebutan string concatenation
// Ada 2 macam string operator, yaitu:
// +
// +=

// Operator +
let namaDepan = "Johan";
let namaBelakang = "Liebert";

console.log(namaDepan + " " + namaBelakang);

// Operator +=
let kata = "Halo ";
kata += "Dunia";

console.log(kata);

// Add
let kata1 = "Kambing saya ada ";
let bilangan = 5;

console.log(kata1 + bilangan);

