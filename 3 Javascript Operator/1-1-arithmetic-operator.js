let bilangan1 = 20;
let bilangan2 = 2;

console.log(bilangan1 + bilangan2); // Output 22
console.log(bilangan1 - bilangan2); // Output 18
console.log(bilangan1 * bilangan2); // Output 40
console.log(bilangan1 / bilangan2); // Output 10
console.log(bilangan1 ** bilangan2); // Output 400
console.log(bilangan1 % bilangan2); // Output 0

// Increment (Tambah 1)
let angka = 10;
angka++;

console.log(angka); // Output 11

// Decrement (Kurang 1)
let angka2 = 22;
angka2--;

console.log(angka2); // Output 21